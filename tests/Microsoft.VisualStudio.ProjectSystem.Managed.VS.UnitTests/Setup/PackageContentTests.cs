    private static IEnumerable<string> GetPackageContents(string vsixName)
    {
        var rootPath = RepoUtil.FindRepoRootPath();

#if DEBUG
        var config = "Debug";
#elif RELEASE
        var config = "Release";
#else
#error Unexpected configuration
#endif

        // D:\repos\project-system\artifacts\Debug\VSSetup\Insertion\ProjectSystem.vsix

        var vsixPath = Path.Combine(
            rootPath,
            "artifacts",
            config,
            "VSSetup",
            "Insertion",
            vsixName);

        using var archive = ZipFile.OpenRead(vsixPath);

        return archive.Entries
            .Select(entry => entry.FullName)
            .Where(path => !path.Contains(DigitalSignature) &&
                           !path.Contains(SettingsRegistrationFileSuffix) &&
                           !path.Contains(Rels) &&
                           !path.Contains("manifest.json"))
            .OrderBy(fn => fn);
    }
